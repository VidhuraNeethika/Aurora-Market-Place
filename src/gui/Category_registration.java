    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String name = jTextField3.getText();

        if (name.isEmpty()) {

            JOptionPane.showMessageDialog(this, "Please enter category name", "Warning", JOptionPane.WARNING_MESSAGE);

        } else {

            try {
                ResultSet rs = MySql.search("SELECT * FROM `category` WHERE `name`='" + name + "'");

                if (rs.next()) {

                    JOptionPane.showMessageDialog(this, "Category already exists", "Warning", JOptionPane.WARNING_MESSAGE);

                } else {

                    Random rnd = new Random();
                    int number = rnd.nextInt(999999);

                    String category_id = "CAT-" + String.format("%06d", number);

                    MySql.iud("INSERT INTO `category` (`id`,`name`) VALUES ('" + category_id + "','" + name + "')");

                    jTextField3.setText("");
                    jTextField3.grabFocus();
                    loadCategories();
                    JOptionPane.showMessageDialog(this, "New category added", "Success", JOptionPane.INFORMATION_MESSAGE);

                }

            } catch (Exception e) {
                e.printStackTrace();
            }

        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        String name = jTextField3.getText();
        int r = jTable1.getSelectedRow();

        if (name.isEmpty()) {

            JOptionPane.showMessageDialog(this, "Please enter category name", "Warning", JOptionPane.WARNING_MESSAGE);

        } else {

            try {

                String category_id = jTable1.getValueAt(r, 0).toString();

                MySql.iud("UPDATE `category` SET `category`.`name`='" + name + "' WHERE `id`='" + category_id + "'");

                jTextField3.setText("");
                jTextField3.grabFocus();
                loadCategories();
                JOptionPane.showMessageDialog(this, "Successfully updated", "Success", JOptionPane.INFORMATION_MESSAGE);

            } catch (Exception e) {
                e.printStackTrace();
            }

        }

    }//GEN-LAST:event_jButton3ActionPerformed


